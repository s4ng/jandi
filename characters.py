def get_word_array(word, weeks_before):
    arr = [[],[],[],[],[],[],[]]
    for i in range(len(word)):
        char_array = CHAR_DICT[word[i]]
        for j in range(0, 7):
            arr[j].extend(char_array[j])
            if i < len(word) - 1:
                arr[j].append(False)
    for i in range(0, weeks_before):
        for j in range(0, 7):
            arr[j].append(False)

    return arr

